     private void loadData()
    {
         clearTable();
        StaffController sc=new StaffController();
        ArrayList<Staff> list=sc.fetchData();
        SimpleDateFormat dFormat;
        SimpleDateFormat = dFormat =new SimpleDateFormat("yyyy-MM-dd");
        String date=dFormat.format(jDateChooser1.getDate()); 
        String[] a = {"Present","Absent"};
        JComboBox c = new JComboBox(a);
        String p="Present";
        for(int i=0;i<list.size();i++)
        {
            TableColumn col= jTable_attendance.getColumnModel().getColumn(3);
            col.setCellEditor(new DefaultCellEditor(c));
            model.addRow(new Object[]
            {
            list.get(i).getStaffId(),
            list.get(i).getFullName(),
           date,p
            });
             
        }
    }

