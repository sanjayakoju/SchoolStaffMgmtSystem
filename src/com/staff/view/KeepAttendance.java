    private void jButton_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_SaveActionPerformed
        // TODO add your handling code here:
        
        ArrayList<Attendance> list=new ArrayList();
         int count=jTable_attendance.getRowCount();
         
         for(int i=0;i<count;i++)
        {
            Attendance ob=new Attendance();
            
            int staffId=Integer.parseInt(jTable_attendance.getValueAt(i,0).toString());
            String staffname=jTable_attendance.getValueAt(i,1).toString();
            
            String date=jTable_attendance.getValueAt(i,2).toString();
            String attendance=jTable_attendance.getValueAt(i,3).toString();
            
            //String d=new java.util.Date().toString();
            
            
            ob.setStaffId(staffId);
            ob.setStaffname(staffname);
            ob.setAttendancedate(date);
            ob.setAttendance(attendance);
           
            
            list.add(ob);
             
        }
        AttendanceController sdc=new AttendanceController();
        sdc.saveData(list);
        clearTable();
    }//GEN-LAST:event_jButton_SaveActionPerformed

